CREATE DEFINER=`root`@`localhost` PROCEDURE `usp_guardar_producto`(
    opcion INT,
    codigo_pro INT,
    descripcionm VARCHAR(40),
    codigo_marca INT,
    codigo_uni INT,
    codigo_categoria INT,
    stock_min INT,
    estadom BOOLEAN
)
BEGIN
    DECLARE codigoProducto INT DEFAULT 0;

    IF opcion = 0 THEN
        -- Insertar un nuevo producto
        INSERT INTO productos (descripcion, cod_marcas, cod_unidad_medida, cod_categoria, stock_minimo, estado)
        VALUES (descripcionm, codigo_marca, codigo_uni, codigo_categoria, stock_min, estadom);

        -- Obtener el código del producto creado
        SET codigoProducto = LAST_INSERT_ID();

        -- Insertar en la tabla stock_producto
        INSERT INTO stock_producto (cod_producto, cod_almacen, stock_actual, precio_uni_compra)
        SELECT codigoProducto, cod_almacen, 0.00, 0.00
        FROM almacen;
    ELSE
        -- Actualizar un producto existente
        UPDATE productos
        SET descripcion = descripcionm,
            cod_marcas = codigo_marca,
            cod_unidad_medida = codigo_uni,
            cod_categoria = codigo_categoria,
            stock_minimo = stock_min,
            estado = estadom
        WHERE cod_producto = codigo_pro;
    END IF;
END ;;
